#include "kmpch.h"
#include "Primitive.h"

namespace Komet
{
    std::shared_ptr<Mesh> Komet::Primitive::Cube()
    {
        static std::shared_ptr<Mesh> cubeMesh = nullptr;

        if (cubeMesh) return cubeMesh;

        // Define cube vertices and indices
        std::vector<Vertex> vertices = {
            // ---------- Front face ----------
            {{-0.5f, -0.5f,  0.5f}, {0, 1, 1}, {0, 0}, {0, 0, 1}},
            {{ 0.5f, -0.5f,  0.5f}, {0, 1, 1}, {1, 0}, {0, 0, 1}},
            {{ 0.5f,  0.5f,  0.5f}, {0, 1, 1}, {1, 1}, {0, 0, 1}},
            {{-0.5f,  0.5f,  0.5f}, {0, 1, 1}, {0, 1}, {0, 0, 1}},

            // ---------- Back face ----------
            {{ 0.5f, -0.5f, -0.5f}, {1, 0, 1}, {0, 0}, {0, 0, -1}},
            {{-0.5f, -0.5f, -0.5f}, {1, 0, 1}, {1, 0}, {0, 0, -1}},
            {{-0.5f,  0.5f, -0.5f}, {1, 0, 1}, {1, 1}, {0, 0, -1}},
            {{ 0.5f,  0.5f, -0.5f}, {1, 0, 1}, {0, 1}, {0, 0, -1}},

            // ---------- Left face ----------
            {{-0.5f, -0.5f, -0.5f}, {1, 1, 0}, {0, 0}, {-1, 0, 0}},
            {{-0.5f, -0.5f,  0.5f}, {1, 1, 0}, {1, 0}, {-1, 0, 0}},
            {{-0.5f,  0.5f,  0.5f}, {1, 1, 0}, {1, 1}, {-1, 0, 0}},
            {{-0.5f,  0.5f, -0.5f}, {1, 1, 0}, {0, 1}, {-1, 0, 0}},

            // ---------- Right face ----------
            {{ 0.5f, -0.5f,  0.5f}, {0, 0, 1}, {0, 0}, {1, 0, 0}},
            {{ 0.5f, -0.5f, -0.5f}, {0, 0, 1}, {1, 0}, {1, 0, 0}},
            {{ 0.5f,  0.5f, -0.5f}, {0, 0, 1}, {1, 1}, {1, 0, 0}},
            {{ 0.5f,  0.5f,  0.5f}, {0, 0, 1}, {0, 1}, {1, 0, 0}},

            // ---------- Top face ----------
            {{-0.5f,  0.5f,  0.5f}, {1, 0, 0}, {0, 0}, {0, 1, 0}},
            {{ 0.5f,  0.5f,  0.5f}, {1, 0, 0}, {1, 0}, {0, 1, 0}},
            {{ 0.5f,  0.5f, -0.5f}, {1, 0, 0}, {1, 1}, {0, 1, 0}},
            {{-0.5f,  0.5f, -0.5f}, {1, 0, 0}, {0, 1}, {0, 1, 0}},

            // ---------- Bottom face ----------
            {{-0.5f, -0.5f, -0.5f}, {0, 1, 0}, {0, 0}, {0, -1, 0}},
            {{ 0.5f, -0.5f, -0.5f}, {0, 1, 0}, {1, 0}, {0, -1, 0}},
            {{ 0.5f, -0.5f,  0.5f}, {0, 1, 0}, {1, 1}, {0, -1, 0}},
            {{-0.5f, -0.5f,  0.5f}, {0, 1, 0}, {0, 1}, {0, -1, 0}},
        };

        std::vector<unsigned int> indices = {
        0, 1, 2,  2, 3, 0,        // Front
        4, 5, 6,  6, 7, 4,        // Back
        8, 9,10, 10,11, 8,        // Left
       12,13,14, 14,15,12,        // Right
       16,17,18, 18,19,16,        // Top
       20,21,22, 22,23,20         // Bottom
        };

        cubeMesh = std::make_shared<Mesh>(vertices, indices);
        return cubeMesh;
    }
}
